module AwesomePrint

  module Colors

    class << self
      attr_accessor :colors
    end

    def self.rgb(*args)
      lookup_by_rgb(args).to_s
    end

    def self.hex(c)
      if c.length != 6 and c.length != 7
        c
      else
        c = "#" + c if c.length == 6
        "fg" + lookup_by_hex(c.upcase).to_s
      end
    end

    def self.lookup_by_rgb(args)
      colors.each do |color|
        return color[0] if color[1] == args
      end
      return 255
    end

    def self.lookup_by_hex(c)
      colors.each do |color|
        return color[0] if color[2] == c
      end
      return 255
    end

    def self.lookup_by_num(n)
      colors[n - 16][2]
    end
    
